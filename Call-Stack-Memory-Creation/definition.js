// Definitions:

// Temporal Dead Zone in Javascript:
// The temporal dead zone is the period of time during which a variable 
// declared with `let` or `const` cannot be accessed. It starts at the 
// beginning of the block scope and ends when the declaration is processed.


// Memory Creation Phase:
// In this phase, the JavaScript engine scans the code and allocates 
// memory for all the variables and functions defined in the current 
// scope. Variables declared with `var` are initialized with `undefined`, 
// while function declarations are stored in their entirety. This is also 
// known as the hoisting mechanism.


// Code Execution Phase:
// Following the memory creation phase, the JavaScript engine executes the code 
// line by line. It assigns the actual values to variables and invokes functions 
// as it encounters them.
