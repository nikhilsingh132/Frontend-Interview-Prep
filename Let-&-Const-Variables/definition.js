// Definitions: 'var' vs 'let' vs 'const'

// var:
// var is function scoped
// var can be redeclared
// var can be updated

// let:
// let is block scoped
// let cannot be redeclared
// let can be updated

// const:
// const is block scoped
// const cannot be redeclared
// const cannot be updated

