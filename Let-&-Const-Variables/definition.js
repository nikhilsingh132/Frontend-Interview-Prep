// ReferenceError is a type of error that occurs when a variable or function is not defined

// TypeError:
// TypeError is a type of error that occurs when an operation is performed on an object
// of an incorrect type

// Uncaught:
// Uncaught is a type of error that occurs when an error is not caught by a try-catch block
