// Definition

// Function statement/ Function declaration:
// It's a standalone construct that declares a function with a specific name,
// parameters, and a body of code to be executed when the function is called.

/*-----------------------------------------------------------------------------------*/

// Function expression:
// A function expression in JavaScript is a way to define a function as part of an
// expression, rather than as a standalone declaration. This means the function is created
// within the context of another expression, often assigned to a variable, passed as an
// argument to another function.

/*-----------------------------------------------------------------------------------*/

// Difference between function statement and function expression:
// Most important difference is hoisting
// In function statement function is hoisted
// In function expression function is not hoisted

/*-----------------------------------------------------------------------------------*/

// Anonymous function:
// An anonymous function is a function without a name. It is often used as a
// callback function or as a function expression assigned to a variable. Used as callback
// functions in higher order functions.

/*-----------------------------------------------------------------------------------*/

// Named function expression:
// It is a type of function expression where the function is given a name after the
// function keyword, in addition to being assigned to a variable.

/*-----------------------------------------------------------------------------------*/

// First class functions:
// In JavaScript, functions are first-class citizens, meaning they can be treated as
// values and assigned to variables, passed as arguments to other functions, and returned
// from functions.

// Special about first class functions:
// 1. Functions can be stored in variables
// 2. Functions can be passed as arguments to other functions
// 3. Functions can be returned from other functions
// 4. Functions can be stored in data structures

/*-----------------------------------------------------------------------------------*/

// Callback function:
// A callback function is a function that is passed as an argument to another function
// and is executed (or "called back") at a later time, typically when an event occurs or
// an operation is completed.