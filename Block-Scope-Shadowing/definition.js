// Definition:

// Block: Block is a pair of curly braces {} and this helps in wrapping up multiple statements
// where javascript code expect a single statement


// Block Scope: Block scope is the region of the code where a variable or function is accessible
// and it is limited to the block in which it is declared. Block scope disappears when the block
// execution is completed and we come out of the block


// Shadowing: Shadowing is the process of a variable or function in a block shadowing a variable
// or function in its parent block