// Definitions:

// Hoisting is a JavaScript mechanism where variable and function declarations
// are moved to the top of their containing scope during the memory creation 
// phase. This means that you can use variables and functions before they are 
// declared in the code.

// Key characteristics of hoisting:

// 1. Variable declarations are hoisted to the top of their scope, but not their 
// initializations.
// 2. Function declarations are hoisted to the top of their scope, and the entire 
// function is hoisted.
// 3. The temporal dead zone (TDZ) is the period of time during which a variable 
// declared with `let` or `const` cannot be accessed. It starts at the beginning 
// of the block scope and ends when the declaration is processed.



// Note:
// Declaration means using the value or function
// Initialization means assigning the value or function