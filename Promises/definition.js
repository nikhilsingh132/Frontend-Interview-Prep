// Definitions:

// In JavaScript, a Promise is an object that represents the eventual 
// completion (or failure) of an asynchronous operation and its resulting 
// value. It acts as a placeholder for a value that may not be available 
// yet but will be resolved at some point in the future. 

/*-----------------------------------------------------------------------------------*/

// A Promise can be in one of three states:

// 1. Pending: The initial state, where the asynchronous operation is still 
// in progress and the result is not yet available.
// 2. Fulfilled (Resolved): The operation completed successfully, and the Promise 
// now holds the resulting value.
// 3. Rejected: The operation failed, and the Promise holds an error reason.