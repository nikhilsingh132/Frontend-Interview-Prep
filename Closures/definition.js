// Definition:

// A closure in JavaScript is the combination of a function and the lexical environment
// within which that function was declared. This means that an inner function "remembers"
// and can access the variables from its outer (enclosing) function's scope, even after the
// outer function has finished executing.

// Closure = function along with its lexical environment bundled together

