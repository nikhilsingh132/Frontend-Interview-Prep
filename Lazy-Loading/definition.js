// Definitions:

// Lazy Loading:
// Lazy loading is a technique where a component is loaded only when it is needed. This is
// useful for improving the performance of an application by reducing the initial bundle size.
// It is also useful for code splitting, which is a technique where a bundle is split into
// smaller chunks, and each chunk is loaded on demand.

// Code Splitting:
// Code splitting is a technique where a bundle is split into smaller chunks, and each chunk
// is loaded on demand. This is useful for improving the performance of an application by
// reducing the initial bundle size.

// <Suspense>:
// <Suspense> is a component that is used to wrap a lazy loaded component. It is used to
// show a fallback UI while the component is loading.
